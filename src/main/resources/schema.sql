DROP TABLE transmissionHeader IF EXISTS;
DROP TABLE fddCases IF EXISTS;

CREATE TABLE transmissionHeader  (
    t_id BIGINT IDENTITY NOT NULL PRIMARY KEY,
    state VARCHAR(20),
    patId VARCHAR(20),
    dtSpec VARCHAR(20),
    calcSerotypeSummary VARCHAR(20)
);

CREATE TABLE fddCases  (
    c_id BIGINT IDENTITY NOT NULL PRIMARY KEY,
    state VARCHAR(20),
    patId VARCHAR(20),
    dtSpec VARCHAR(20),
    calcSerotypeSummary VARCHAR(20)
);
